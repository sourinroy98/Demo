package com.test.Java_Assignments;

public class RelationalOperator {

	public static void main(String[] args) {
		
		int a=3,b=7;
		
		System.out.println(a==b);
		
		System.out.println(a!=b);
		
		System.out.println(a>b);
		
		System.out.println(a<b);
		
		System.out.println(a>=b);
		
		System.out.println(a<=b);

	}

}
